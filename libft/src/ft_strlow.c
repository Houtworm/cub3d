#include "../libft.h"

char	*ft_strlow(char *s)
{
	char	r[5000];
	int		i;

	i = 0;
	while (s[i])
	{
		r[i] = ft_tolower(s[i]);
		i++;
	}
	r[i] = '\0';
	s = r;
	return (s);
}
